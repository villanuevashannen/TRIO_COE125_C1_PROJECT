    def search(self):
        conn = sqlite3.connect('CiscoLab.db')
        with conn:
            c = conn.cursor()
        c.execute("SELECT * FROM StudentInfo WHERE StudentNumber=:sn", {'sn':self.lineStudNum.text()})
        info = c.fetchone()
        c.execute("SELECT * FROM Borrowed WHERE StudentNumber=:sn", {'sn':self.lineStudNum.text()})
        items = c.fetchone()
        if info is None:
            QMessageBox.about(self, "Warning", "Student not Found!")
            self.clear()
            self.lineStudNum.setText('')
            self.foundctr = 0
        else:
            
            self.labelName.setText("NAME: " + info[0])
            self.labelCourse.setText("COURSE: " + info[2])
            self.labeRoom.setText("ROOM: " + info[3])
            self.labelProfessor.setText("PROFESSOR: " + info[4])
            self.labelRack.setText("RACK = " + str(items[1]))
            self.labelSwitch.setText("SWITCH = " + str(items[2]))
            self.labelRouter.setText("ROUTER = " + str(items[3]))
            self.labelConsoleCable.setText("CONSOLE CABLE = " + str(items[4]))
            self.lanelLanCable.setText("LAN CABLE = " + str(items[5]))
            self.labelPowerCable.setText("POWER CABLE = " + str(items[6]))
            self.foundctr = 1
    

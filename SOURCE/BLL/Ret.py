    def remove(self):
        st = self.lineStudNum.text()
        Student.removeStudInfo(st)
        Inventory.returnItems(st)
        self.clear()
        self.foundctr = 0
        QMessageBox.about(self, "Cisco Borrowing System", "Return Success!")
        
